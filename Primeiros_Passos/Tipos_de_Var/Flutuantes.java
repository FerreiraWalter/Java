public class Flutuantes {

    public static void main(String[] args) {

        System.out.println(2.1);
        System.out.println(1.1);
        System.out.println(0.1);
        System.out.println(-0.1);
        System.out.println(-1.1);
        System.out.println(-2.1);

        //Tipo Float:
        System.out.println(2.2f);
        System.out.println(1.2f);
        System.out.println(1f);
        System.out.println(-1.2f);
        System.out.println(-2.2f);    
    }
}